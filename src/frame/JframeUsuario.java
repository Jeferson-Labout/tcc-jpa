    private void tblUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuarioMouseClicked
        int linha = this.tblUsuario.getSelectedRow();
        String id = this.tblUsuario.getValueAt(linha, 0).toString();
        String nome = this.tblUsuario.getValueAt(linha, 1).toString();
        String senha = this.tblUsuario.getValueAt(linha, 2).toString();
        String tipo = this.tblUsuario.getValueAt(linha, 3).toString();

        lblCodigo.setText(id);
        txtLogin.setText(nome);
        txtSenha.setText(senha);

        if ("2".equals(tipo)) {
            this.chk_usuario.setSelected(true);
            this.chk_coletor.setSelected(false);
        } else {
            this.chk_coletor.setSelected(true);
            this.chk_usuario.setSelected(false);
        }


    }//GEN-LAST:event_tblUsuarioMouseClicked

