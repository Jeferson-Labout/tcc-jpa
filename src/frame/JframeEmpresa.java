    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        if (this.lblCodigo.getText().isEmpty()) {

            String razao = txtRazao.getText();
            String email = txtEmail.getText();
            String cnpj = txtCNPJ.getText();
            String telefone = txtTelefone.getText();
            try {
                em.getTransaction().begin();
                Empresa empresa = new Empresa();
                empresa.setRazaoSocial(razao);
                empresa.setEmail(email);
                empresa.setCnpj(formataDados(cnpj));
                empresa.setTelefone(formataDados(telefone));
                em.persist(empresa);
                em.flush();
                em.getTransaction().commit();

                JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso!!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                System.out.println(e);

            }
        } else {
            String razao = txtRazao.getText();
            String email = txtEmail.getText();
            String cnpj = txtCNPJ.getText();
            String telefone = txtTelefone.getText();
            try {
                String codigo = lblCodigo.getText();
                em.getTransaction().begin();
                Empresa empresa = em.find(Empresa.class, Long.parseLong(codigo));
                empresa.setRazaoSocial(razao);
                empresa.setEmail(email);
                empresa.setCnpj(formataDados(cnpj));
                empresa.setTelefone(formataDados(telefone));
                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Atualizado com Sucesso!!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                System.out.println(e);

            }
        }

        CapturarTeclado();
    }//GEN-LAST:event_btnGravarActionPerformed

    public void CapturarTeclado() {
        lblCodigo.setVisible(false);
        btnHbCampos.setVisible(true);
        txtRazao.setEnabled(false);
        txtCNPJ.setEnabled(false);
        txtEmail.setEnabled(false);
        txtTelefone.setEnabled(false);

    }


    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        List<Empresa> empresas = em.createNativeQuery("SELECT  *FROM Empresa  WHERE id_empresa = 1", Empresa.class).getResultList();
        for (Empresa empresa : empresas) {

            lblCodigo.setText(empresa.getIdEmpresa().toString());
            txtRazao.setText(empresa.getRazaoSocial());
            txtCNPJ.setText(empresa.getCnpj());
            txtEmail.setText(empresa.getEmail());
            txtTelefone.setText(empresa.getTelefone());
            if (this.lblCodigo.getText().isEmpty()) {

            } else {
                lblCodigo.setVisible(false);
                btnHbCampos.setVisible(true);
                txtRazao.setEnabled(false);
                txtCNPJ.setEnabled(false);
                txtEmail.setEnabled(false);
                txtTelefone.setEnabled(false);

            }
        }
    }//GEN-LAST:event_formWindowOpened

