    public static String formataDados(String dado) {
        dado = dado.replaceAll("\\.", "");
        dado = dado.replaceAll("-", "");
        dado = dado.replaceAll("[()]", "");
        dado = dado.replaceAll("[ ]", "");
        return dado;
    }

