    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        if (this.lblCodigo.getText().isEmpty()) {
            try {
                String login = this.txtLogin.getText();
                int tipocad = 0;
                String senha = this.txtSenha.getText();

                if (this.chk_usuario.isSelected()) {
                    tipocad = 2;
                } else {
                    tipocad = 3;
                }

                em.getTransaction().begin();
                Cliente cliente = new Cliente();
                cliente.setClinome(login);
                cliente.setClisenhausuario(senha);
                cliente.setClitipocad(tipocad);

                //salva no banco
                em.persist(cliente);
                em.flush();
                //retorna o ultimo
                int id = cliente.getCliid();
                System.out.println(cliente.getCliid());
                JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso!!");
                atualizatabela();
                em.getTransaction().commit();
                new LimpaTela().LimpaTela(jPanel2);
                new LimpaTela().LimpaTela(jPanel3);
                lblCodigo.setText("");

            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);

            }
        } else {

            String codigo = this.lblCodigo.getText();

            try {
                String login = this.txtLogin.getText();
                int tipocad = 0;
                String senha = this.txtSenha.getText();

                if (this.chk_usuario.isSelected()) {
                    tipocad = 2;
                } else {
                    tipocad = 3;
                }

                em.getTransaction().begin();
                Cliente cliente = em.find(Cliente.class, Integer.parseInt(codigo));
                cliente.setClinome(login);
                cliente.setClisenhausuario(senha);
                cliente.setClitipocad(tipocad);

                JOptionPane.showMessageDialog(null, "Atualizado com Sucesso!!");
                em.getTransaction().commit();
                atualizatabela();
                new LimpaTela().LimpaTela(jPanel2);
                new LimpaTela().LimpaTela(jPanel3);
                lblCodigo.setText("");

            } catch (HeadlessException | NumberFormatException e) {
                JOptionPane.showMessageDialog(null, e);

            }

        }
    }//GEN-LAST:event_btnGravarActionPerformed
    public void atualizatabela() {

        DefaultTableModel model = (DefaultTableModel) this.tblUsuario.getModel();
        //limpa a tabela
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        //faz select de todos os dados e armazena em uma lista
        List clientes = em.createNamedQuery("Cliente.findByUsuario2", Cliente.class).getResultList();
        //pega todos os contato e adiciona na tebela.
        for (int i = 0; i < clientes.size(); i++) {
            model.addRow(new Object[]{
                ((Cliente) clientes.get(i)).getCliid(),
                ((Cliente) clientes.get(i)).getClinome(),
                ((Cliente) clientes.get(i)).getClisenhausuario(),
                ((Cliente) clientes.get(i)).getClitipocad()
            });
        }

    }


