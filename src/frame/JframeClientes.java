    private void forCepKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_forCepKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            ViaCEP viaCep = new ViaCEP();

            try {
                viaCep.buscar(formataDados(forCep.getText()));
                txtBairro.setText(viaCep.getBairro());
                txtEndereco.setText(viaCep.getLogradouro());
                ComboBoxUF.setSelectedItem(viaCep.getUf());
                ComboBoxCidade.setSelectedItem(viaCep.getLocalidade());
            } catch (ViaCEPException ex) {
                Logger.getLogger(JframeClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (evt.getKeyCode() == evt.VK_ENTER) {
            txtNumero.requestFocus();
        }
    }//GEN-LAST:event_forCepKeyPressed
