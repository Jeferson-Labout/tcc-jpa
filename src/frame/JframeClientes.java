    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String codigo = this.txtCodigo.getText();
        String nome = this.txtRazao1.getText().toUpperCase();
        DefaultTableModel model = (DefaultTableModel) this.tblClientes.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }

        String sql = "SELECT *FROM Cliente  WHERE 1=1 and clitipocad = 1 ";

        if (!"".equals(nome)) {
            sql += " and clinome like '%" + nome + "%'";

        }
        if (!"".equals(codigo)) {
            sql += " and cliid in (" + codigo + ")";

        }

        try {

            List<Cliente> cliente = em.createNativeQuery(sql, Cliente.class).getResultList();
            DefaultTableModel dados = (DefaultTableModel) tblClientes.getModel();
            dados.setNumRows(0);
            for (Cliente c : cliente) {
                dados.addRow(new Object[]{
                    c.getCliid(),
                    c.getClinumerocontrato(),
                    c.getClirotadescricao(),
                    c.getClinome(),
                    c.getClifantasia(),
                    c.getClicpfcgc(),
                    c.getClicidade(),
                    c.getCliuf(),
                    c.getClitipo(),
                    c.getClibairro(),
                    c.getClifone(),
                    c.getClilatitude(),
                    c.getClilongitude(),
                    c.getClinumero(),
                    c.getCliendereco(),
                    c.getCliemail(),
                    c.getClivalorfixo(),
                    c.getClivalorexcedente(),
                    c.getCliqtdecontratada()
                });

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Usuario não encontrado");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

